        public static Map3D Reference(Map3D y, Filter3D w, int inw, int inh, int ind, int kwidth, int kheight, int kdepth, int stride) {
            int inchannels = w.InChannels, outchannels = w.OutChannels, batch = y.Batch;
            int outw = (inw - kwidth) / stride + 1, outh = (inh - kheight) / stride + 1, outd = (ind - kdepth) / stride + 1;

            if (y.Width != outw || y.Height != outh || y.Depth != outd) {
                throw new ArgumentException("mismatch shape");
            }

            Map3D x = new Map3D(inchannels, inw, inh, ind, batch);

            for (int kx, ky, kz = 0; kz < kdepth; kz++) {
                for (ky = 0; ky < kheight; ky++) {
                    for (kx = 0; kx < kwidth; kx++) {
                        for (int th = 0; th < batch; th++) {
                            for (int ox, oy, oz = 0; oz < outd; oz++) {
                                for (oy = 0; oy < outh; oy++) {
                                    for (ox = 0; ox < outw; ox++) {
                                        for (int outch = 0; outch < outchannels; outch++) {
                                            double v = y[outch, ox, oy, oz, th];

                                            for (int inch = 0; inch < inchannels; inch++) {
                                                x[inch, kx + ox * stride, ky + oy * stride, kz + oz * stride, th] += v * w[inch, outch, kx, ky, kz];
                                            }
                                        }
                                    }
                                }
                            }
                        }
                    }
                }
            }

            return x;
        }

        public static Map3D OptimizedReference(Map3D y, Filter3D w, int inw, int inh, int ind, int kwidth, int kheight, int kdepth, int stride) {
            int inchannels = w.InChannels, outchannels = w.OutChannels, batch = y.Batch;
            int outw = (inw - kwidth) / stride + 1, outh = (inh - kheight) / stride + 1, outd = (ind - kdepth) / stride + 1;

            if (y.Width != outw || y.Height != outh || y.Depth != outd) {
                throw new ArgumentException("mismatch shape");
            }

            Map3D x = new Map3D(inchannels, inw, inh, ind, batch);

            for (int kx, ky, kz = 0; kz < kdepth; kz++) {
                for (ky = 0; ky < kheight; ky++) {
                    for (kx = 0; kx < kwidth; kx++) {
                        int outmap_offset = (kx + ky * inw + kz * inw * inh) * inchannels;
                        int kernel_offset = (kx + ky * kwidth + kz * kwidth * kheight) * inchannels * outchannels;

                        for (int th = 0; th < batch; th++) {
                            for (int ox, oy, oz = 0; oz < outd; oz++) {
                                for (oy = 0; oy < outh; oy++) {
                                    for (ox = 0; ox < outw; ox++) {
                                        int inmap_org = (ox + oy * outw + oz * outw * outh) * outchannels + th * outw * outh * outd * outchannels;
                                        int outmap_idx = outmap_offset + (ox + oy * inw + oz * inw * inh) * inchannels * stride + th * inw * inh * ind * inchannels;
                                        int kernel_org = kernel_offset;
                                        double[] temp = new double[4];

                                        int inch;

                                        for (inch = 0; inch < inchannels - inchannels % 4; inch += 4) {
                                            temp[0] = x[outmap_idx];
                                            temp[1] = x[outmap_idx + 1];
                                            temp[2] = x[outmap_idx + 2];
                                            temp[3] = x[outmap_idx + 3];

                                            int inmap_idx = inmap_org;
                                            int kernel_idx = kernel_org;

                                            for (int outch = 0; outch < outchannels; outch++) {
                                                double v = y[inmap_idx];

                                                temp[0] += v * w[kernel_idx];
                                                temp[1] += v * w[kernel_idx + 1];
                                                temp[2] += v * w[kernel_idx + 2];
                                                temp[3] += v * w[kernel_idx + 3];

                                                inmap_idx++;
                                                kernel_idx += inchannels;
                                            }

                                            x[outmap_idx] = temp[0];
                                            x[outmap_idx + 1] = temp[1];
                                            x[outmap_idx + 2] = temp[2];
                                            x[outmap_idx + 3] = temp[3];

                                            outmap_idx += 4;
                                            kernel_org += 4;
                                        }

                                        if (inchannels % 4 != 0) {
                                            int sets = inchannels % 4;

                                            for (int i = 0; i < sets; i++) {
                                                temp[i] = x[outmap_idx + i];
                                            }

                                            int inmap_idx = inmap_org;
                                            int kernel_idx = kernel_org;

                                            for (int outch = 0; outch < outchannels; outch++) {
                                                double v = y[inmap_idx];

                                                for (int i = 0; i < sets; i++) {
                                                    temp[i] += v * w[kernel_idx + i];
                                                }

                                                inmap_idx++;
                                                kernel_idx += inchannels;
                                            }

                                            for (int i = 0; i < sets; i++) {
                                                x[outmap_idx + i] = temp[i];
                                            }
                                        }
                                    }
                                }
                            }
                        }
                    }
                }
            }

            return x;
        }

